// Add operation
const add = (a, b) => {
  return a + b;
};

// Subtract operation
const subtract = (a, b) => {
  return a - b;
};

// Multiplication operation
const multiply = (a, b) => {
  return a * b;
};

// Division operation
const division = (a, b) => {
  return b === 0 ? "Math error" : a / b;
};